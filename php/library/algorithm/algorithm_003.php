/**
 * 递归求n的阶层
 * @param $n 总阶数，默认10
 * @return 递归返回 int
 */
function fact($n = 10) {
	if ($n > 50) {
		echo "n阶数超过50，为防止计算机死机终止执行！";
		exit();
	}
	if ($n == 0 || $n == 1) {
		return 1;
	} else {
		return $n*fact($n-1);
	}
}
