<?php
/**
 +-----------------------------------------------------------------------------
 * @ 猴子吃桃子问题
 +-----------------------------------------------------------------------------
 * 算法题目：一只猴子摘了若干桃子，每天吃现有桃的一半多一个，到第10天时就只有
 * 一个桃子，求原有多少桃子。
 +-----------------------------------------------------------------------------
 * 数学模型：每天的桃子数为
 * a10 =1,a9=(1+a10)*2,a8=(1+a9)*2,...
 * 递推公式为:
 * ai = (1 + a(i+1))*2 i=9,8,7,...
 +-----------------------------------------------------------------------------
 * 算法设计：由于每天的桃子数只依赖前一天的桃子数，所以用一个迭代变量代表桃子
 * 个数就可以了
 +-----------------------------------------------------------------------------
 */
 
$a = 1;
for ($i=9; $i >=1; $i=$i-1) { 
	$a = ($a+1)*2;
}
echo $a;